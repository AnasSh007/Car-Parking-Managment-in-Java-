    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String username = jTextField1.getText().trim();
        String password = jPasswordField1.getText().trim();

        ResultSet resultset = null;
        int user_exist=0;
        int result=0;
        
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/cpm","root","");
            

            String selectSql = "SELECT * from users where username = '"+username+"'";
            Statement statement = connection.createStatement();
            resultset = statement.executeQuery(selectSql);
            
            while(resultset.next()){
                if (username.equals(resultset.getString(2)))
                {
                    user_exist=1;
                }
            }
            
            if (user_exist!=1)
            {
                String sql = "INSERT INTO users(username,pass)VALUES(?,?)";
                PreparedStatement statement2 = connection.prepareStatement(sql);
                statement2.setString(1,username);
                statement2.setString(2,password);
                result = statement2.executeUpdate();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"User with '"+username+"' already exists");
                jTextField1.setText("");
                jPasswordField1.setText("");
            }
                
            
            if(result>0)
            {
                JOptionPane.showMessageDialog(null,"User Added");
                close();
                mainPage mPage = new mainPage();
                mPage.show(true);
                
            }  
           
        }
        
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Something Went Wrong");
        }

       
    }//GEN-LAST:event_jButton1ActionPerformed

